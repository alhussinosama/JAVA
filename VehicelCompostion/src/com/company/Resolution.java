package com.company;

public class Resolution {
    private int width;
    private int height;

    // generate constructor

    public Resolution(int width, int height) {
        this.width = width;
        this.height = height;
    }
    // generate a getter

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }
}
